// Функция получения факта на основе текущего часа
function getFactForCurrentHour() {
    const hoursSinceEpoch = Math.floor(Date.now() / 3600000);
    const index = hoursSinceEpoch % HOURLY_FACTS.length;
    return HOURLY_FACTS[index];
}

// Функция получения случайного факта (для кнопки сброса/обновления)
function getRandomFact(excludeIndex = -1) {
    if (HOURLY_FACTS.length <= 1) return HOURLY_FACTS[0];
    let index;
    do {
        index = Math.floor(Math.random() * HOURLY_FACTS.length);
    } while (index === excludeIndex);
    return { fact: HOURLY_FACTS[index], index };
}
